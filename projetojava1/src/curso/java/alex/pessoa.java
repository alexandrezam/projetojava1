package curso.java.alex;

public class pessoa {

	private string Nome;
	private string Sobrenome;
	
	private int Idade;
	
	public int getIdade() {
		return Idade;
	}
	public void setIdade(int idade) {
		Idade = idade;
	}
	public string getNome() {
		return Nome;
	}
	public void setNome(string nome) {
		Nome = nome;
	}
	public string getSobrenome() {
		return Sobrenome;
	}
	public void setSobrenome(string sobrenome) {
		Sobrenome = sobrenome;
	}
	
	
}
